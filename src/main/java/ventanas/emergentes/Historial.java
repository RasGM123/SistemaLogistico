    private void BuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BuscarActionPerformed
        try {
            String nom = DNI.getText();
            this.user = sis.buscarUsuarioPorDni(nom);
            DefaultTableModel model = (DefaultTableModel) his.getModel();
            Usuario us = sis.obtenerSesion();
            if(us instanceof Administrativo admin){
                List<Pedido> lp = admin.listarPedidosCliente((Cliente)user);
                for(Pedido p: lp){
                    Object[] m = new Object[3];
                    m[0] = p.getId();
                    m[1] = p.getFechaCreacion().toString();
                    m[2] = p.getEstado();
                    model.addRow(m);
                }
                model.fireTableDataChanged();
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: Entrada de datos no correspondientes");
        }
    }//GEN-LAST:event_BuscarActionPerformed


