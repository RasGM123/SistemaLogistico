    private void selectitemlist() {
        ListaTickets.addMouseListener(new MouseAdapter() {
            @Override
            public void mouseClicked(MouseEvent evt) {
                int index = ListaTickets.rowAtPoint(evt.getPoint());
                int id = (int) ListaTickets.getValueAt(index, 0);
                Usuario us = sis.obtenerSesion();
                if (us instanceof Cliente cli) {
                    Ticket t = cli.buscarTicket(id);
                    respuesta.setText(t.getRespuesta());
                }
            }
        });
    }
    
    private void setLista(){
        Usuario us = sis.obtenerSesion();
        DefaultTableModel model = (DefaultTableModel) ListaTickets.getModel();
        if(us instanceof Cliente cli){
            List<Ticket> ts = cli.getTickets();
            for(Ticket t: ts){
                Object[] tc = new Object[2];
                tc[0] = t.getId();
                tc[1] = t.getMotivo();
                model.addRow(tc);
            }
            model.fireTableDataChanged();
        }
    }

